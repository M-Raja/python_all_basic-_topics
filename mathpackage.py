# math package in python 
# math with inbuild functions
import math
print(math.sqrt(4)) #----> square root
print(math.ceil(1.555)) #---> (2) whole value
print(math.floor(1.555)) #---> (1) base value
print(math.factorial(5)) #----> factorial
print(math.fabs(-5)) #----> absolute value
print(math.fabs(5)) #----> absolute value
print(math.pow(2,3)) #----> 2 power 3
print(math.log2(2)) #----->1.0
print(math.log10(2)) #----->0.3010
#constant value
print(math.pi) #---->3.1415     
print(math.e) #---->2.718